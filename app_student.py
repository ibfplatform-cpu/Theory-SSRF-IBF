# --- 1a: K_cf ---
with st.expander("⚡ 1а. K_cf — Свежая конфликтная травма (диапазон 0–0.40)", expanded=False):
    st.markdown("""<div class="guide-box">
<b>Что это:</b> Поправка на резкое новое обострение в последние 4 недели, которое ещё не успело
отразиться в долгосрочных оценках маркеров.<br>
<b>Когда ставить > 0:</b> Только если произошло что-то НОВОЕ и резкое — военная эскалация,
теракт, переворот, массовые беспорядки. Если конфликт идёт давно и уже отражён в маркерах I₅ — K_cf ≈ 0.<br>
<b>Ключевой вопрос: что я фиксирую в K_cf, чего ещё нет в I₅?</b>
</div>""", unsafe_allow_html=True)

    st.markdown("**Шаг 1. Соберите данные ACLED** → [acleddata.com](https://acleddata.com) "
                "→ выберите страну → период: последние 30 дней → подсчитайте число событий.")

    col1, col2, col3 = st.columns(3)
    with col1:
        v_current = st.number_input("Жертв/событий за последние 30 дней (V_current):",
                                     0, 100000, 0, key="kcf_vcur",
                                     help="По методологии IBF используется число жертв (fatalities) из ACLED. "
                                          "Если данные по жертвам недоступны — допустимо число событий, "
                                          "но μ₁₂ и σ₁₂ должны быть рассчитаны по той же метрике.")
    with col2:
        mu_12 = st.number_input("Среднее за 12 месяцев (μ₁₂):", 0.0, 100000.0, 10.0, step=1.0, key="kcf_mu")
    with col3:
        sigma_12 = st.number_input("Стандартное отклонение σ₁₂:", 0.1, 10000.0, 5.0, step=0.5, key="kcf_sig")

    if sigma_12 > 0:
        # Предупреждение: при низкой интенсивности конфликта (μ<10 и σ<0.5) σ-нормализация
        # может давать экстремальные ΛIntensity при незначительных колебаниях.
        # В таком контексте ориентируйтесь на абсолютный прирост инцидентов, а не на z-оценку.
        if mu_12 < 10 and sigma_12 < 0.5:
            st.warning("⚠️ Низкоинтенсивный контекст (μ₁₂ < 10, σ₁₂ < 0.5): "
                       "z-нормализация нестабильна при малых числах. "
                       "Проверьте результат вручную: абсолютное изменение "
                       f"({v_current} − {mu_12:.0f} = {v_current - mu_12:+.0f} инцид.) "
                       "должно ощущаться как реальное обострение, прежде чем выставлять K_cf > 0.10.")

        lambda_i = (v_current - mu_12) / sigma_12
        k_cf_calc = round(0.40 / (1 + math.exp(-2 * lambda_i)), 3)
        if lambda_i <= 0:
            st.warning(
                f"⚠️ ΛIntensity = {lambda_i:.2f} ≤ 0: конфликт не растёт или снижается относительно "
                "12-месячного среднего. Формула даёт K_cf > 0, но методологически «свежей травмы» нет. "
                "Рекомендуется установить K_cf = 0.00 (или 0.01–0.02) в боковой панели, "
                "если нет отдельных качественных оснований."
            )
        st.markdown(f"""<div class="calc-box">
<b>Шаг 2 (автоматический расчёт):</b><br>
ΛIntensity = ({v_current} − {mu_12}) / {sigma_12} = <b>{lambda_i:.2f}</b><br>
K_cf = 0.40 / (1 + e^(−2·{lambda_i:.2f})) = <b>{k_cf_calc}</b><br>
→ Установите это значение в боковой панели (K_cf)
</div>""", unsafe_allow_html=True)
        coeff_data["k_cf_lambda"] = lambda_i
        coeff_data["k_cf_suggested"] = k_cf_calc

    st.markdown("""<div class="ex-box">
<b>Ориентиры для проверки:</b><br>
• K_cf ≈ 0: Турция декабрь 2021 — ΛIntensity &lt;&lt; –2σ; конфликт значительно ниже 12-месячного среднего, нового обострения нет.<br>
• K_cf ≈ 0.10: ΛIntensity ≈ –0.5 — текущий конфликт умеренно <b>ниже</b> среднего; поправка минимальна.<br>
• K_cf ≈ 0.22: ΛIntensity ≈ +0.1 — конфликт чуть выше базового уровня; умеренный сигнал.<br>
• K_cf ≈ 0.35: ΛIntensity ≈ +1σ — значительное обострение; вдвое выше нормы.<br>
• K_cf = 0.40: Пиковый кризис. Ливия февраль 2011 — гражданская война, удары НАТО (ΛIntensity >> +2σ).
</div>""", unsafe_allow_html=True)

    k_cf_source = st.text_input("🔗 Источник данных (ACLED, GEC и др.):", key="kcf_src",
                                 placeholder="https://acleddata.com/...")
    k_cf_fact = st.text_area("Фактология: что именно происходит? Почему выставлено именно такое значение?",
                              key="kcf_fact",
                              placeholder="Пример: По данным ACLED за декабрь 2021 в Турции зафиксировано 3 события "
                                          "(нормальный уровень ~5/мес, σ≈3). ΛIntensity ≈ –0.67, K_cf ≈ 0.05 — "
                                          "нет нового обострения, выставляем минимум.")
    coeff_data.update({"k_cf_source": k_cf_source, "k_cf_fact": k_cf_fact,
                        "k_cf_v_current": v_current, "k_cf_mu12": mu_12, "k_cf_sigma12": sigma_12})

