def _ibf_draft_dump():
    """Собирает сериализуемые значения session_state для черновика."""
    _skip = {"draft_uploader"}
    _skip_pfx = ("_ibf_draft_",)
    out = {}
    for k, v in st.session_state.items():
        if k in _skip or any(k.startswith(p) for p in _skip_pfx):
            continue
        try:
            json.dumps({k: v})
            out[k] = v
        except (TypeError, ValueError):
            pass
    return out


with st.expander("📂 Черновик: загрузить / сохранить", expanded=False):
    st.caption(
        "Сохраните черновик в любой момент — файл скачается на ваш компьютер. "
        "Чтобы продолжить позже, откройте опросник заново и загрузите этот файл: "
        "все ползунки и поля вернутся на свои места."
    )
    _d_col1, _d_col2 = st.columns(2)

    with _d_col1:
        st.markdown("**📤 Загрузить черновик:**")
        _uploaded_draft = st.file_uploader(
            "Файл черновика (.json)",
            type=["json"],
            key="draft_uploader",
            label_visibility="collapsed",
        )
        if _uploaded_draft is not None:
            _fid = f"_ibf_draft_{_uploaded_draft.name}_{_uploaded_draft.size}"
            if not st.session_state.get(_fid, False):
                try:
                    _draft_content = json.load(_uploaded_draft)
                    _draft_ss = _draft_content.get("session_state", {})
                    _n_restored = 0
                    for _dk, _dv in _draft_ss.items():
                        if _dk not in {"draft_uploader"} and not _dk.startswith("_ibf_draft_"):
                            st.session_state[_dk] = _dv
                            _n_restored += 1
                    st.session_state[_fid] = True
                    st.success(
                        f"✅ Черновик от {_draft_content.get('saved_at', '—')} загружен. "
                        f"Восстановлено значений: {_n_restored}. "
                        "Прокрутите страницу вниз — все данные на месте."
                    )
                except Exception as _draft_err:
                    st.error(f"Не удалось загрузить черновик: {_draft_err}")
            else:
                st.info(f"Черновик «{_uploaded_draft.name}» уже применён в этой сессии.")

    with _d_col2:
        st.markdown("**💾 Сохранить черновик:**")
        _draft_payload = json.dumps(
            {
                "version": "IBF_draft_v1",
                "saved_at": str(datetime.now()),
                "session_state": _ibf_draft_dump(),
            },
            ensure_ascii=False,
            indent=2,
        )
        st.download_button(
            label="⬇️ Скачать черновик",
            data=_draft_payload,
            file_name="IBF_черновик.json",
            mime="application/json",
            help=(
                "Скачает текущее состояние всех полей. "
                "При следующем открытии загрузите этот файл — всё восстановится."
            ),
        )

